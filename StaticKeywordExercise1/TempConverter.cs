using System;
using System.Collections.Generic;
using System.Text;

namespace StaticKeywordExercise1
{
    class TempConverter
    {
        public static double FarenheitToCelsius(double f)
        {
            return f * -17.222222;
        }
        public static double CelsiusToFarenheit(double c)
        {
            return c * 33.8;
        }
    }
}
